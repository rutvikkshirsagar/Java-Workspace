

public class learn_switch {
    public static void main(String args[]){
        int var = 4;
        switch(var){
            case 1:
                System.out.println("1");
                break;
            case 2:
                System.out.println("2");
                break;
            case 3:
                System.out.println("3");
                break;
            default:
                System.out.println("default");
        }
    }
}
