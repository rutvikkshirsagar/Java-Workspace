package Loops;

public class program1 {
    public static void main(String args[]){

        //print number from 1 to 10

        for(int i = 1; i<=10; i++){
            System.out.print(i + " ");
        }
        System.out.println();

        int x = 1;
        while(x<=10){
            System.out.print(x +" ");
            x++;
        }
    }
}
